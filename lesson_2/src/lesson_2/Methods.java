package lesson_2;

public class Methods {
    public int compare(int a, int b) {
        return (a > b) ? b : a;
    }

    public boolean evenNumber(int c) {
        return (c % 2 == 0) ? true : false;
    }

    public int squareOfNumber(int d) {
        return d * d;
    }

    public int cubeOfNumber(int e) {
        return e * e * e;
    }
}
